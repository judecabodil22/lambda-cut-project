#!/usr/bin/env python3
"""
Lambda Cut Kdenlive Automation - Simplified
Generates Kdenlive project file with all assets.
"""

import os
import sys
import json
import glob
import subprocess
from datetime import datetime


# ==============================
# Configuration
# ==============================

FRAME_RATE = 25
# Use the latest output file as base project if no specific base exists
OUTPUT_DIR = "/home/alph4r1us/Documents/Black-Mesa Laboratories/lambda-cut/output"
ASS_STYLE = "Style: Default,Fira Sans Ultra,70.00,&H00FFFFFF,&H000000FF,&H00000000,&H00000000,-1,0,0,0,100.00,100.00,0.00,0.00,1,10.00,11.00,5,40,40,71,1"

def find_base_project():
    """Find the latest Kdenlive project to use as template."""
    output_dir = OUTPUT_DIR
    if os.path.exists(output_dir):
        projects = sorted(glob.glob(os.path.join(output_dir, "*.kdenlive")))
        if projects:
            return projects[-1]  # Return the latest
    return None


# ==============================
# Data Collection
# ==============================

def get_script_titles(workspace):
    scripts_dir = os.path.join(workspace, "scripts")
    titles = {}
    for script_file in sorted(glob.glob(os.path.join(scripts_dir, "script_*.txt"))):
        basename = os.path.basename(script_file)
        script_num = int(basename.replace("script_", "").replace(".txt", ""))
        with open(script_file) as f:
            first_line = f.readline().strip()
            if "Title:" in first_line:
                title = first_line.split("Title:", 1)[1].strip()
                if ":" in title:
                    title = title.split(":", 1)[1].strip()
            else:
                title = first_line
        titles[script_num] = title
    return titles


def get_tts_durations(workspace):
    tts_dir = os.path.join(workspace, "tts")
    durations = {}
    for wav_file in sorted(glob.glob(os.path.join(tts_dir, "tts_*.wav"))):
        basename = os.path.basename(wav_file)
        script_num = int(basename.replace("tts_", "").replace(".wav", ""))
        try:
            result = subprocess.run(
                ["ffprobe", "-v", "error", "-show_entries", "format=duration",
                 "-of", "default=noprint_wrappers=1:nokey=1", wav_file],
                capture_output=True, text=True, check=True
            )
            durations[script_num] = float(result.stdout.strip())
        except (subprocess.CalledProcessError, ValueError):
            durations[script_num] = 0
    return durations


def parse_srt_file(srt_file):
    subtitles = []
    if not os.path.exists(srt_file):
        return subtitles
    with open(srt_file) as f:
        content = f.read()
    blocks = content.strip().split("\n\n")
    for block in blocks:
        lines = block.strip().split("\n")
        if len(lines) >= 3:
            timing = lines[1].strip()
            if "-->" in timing:
                start_str, end_str = timing.split("-->")
                start = parse_srt_time(start_str.strip())
                end = parse_srt_time(end_str.strip())
                text = " ".join(lines[2:]).strip()
                subtitles.append({"start": start, "end": end, "text": text})
    return subtitles


def parse_srt_time(time_str):
    time_str = time_str.replace(",", ".")
    parts = time_str.split(":")
    return int(parts[0]) * 3600 + int(parts[1]) * 60 + float(parts[2])


def seconds_to_ass_time(seconds):
    hours = int(seconds // 3600)
    minutes = int((seconds % 3600) // 60)
    secs = seconds % 60
    centiseconds = int((secs % 1) * 100)
    secs = int(secs)
    return f"{hours}:{minutes:02d}:{secs:02d}.{centiseconds:02d}"


def seconds_to_frames(seconds):
    return int(seconds * FRAME_RATE)


def generate_ass_subtitles(workspace, script_titles, tts_durations):
    ass_content = []
    ass_content.append("[Script Info]")
    ass_content.append("; Script generated by Lambda Cut Kdenlive Automation")
    ass_content.append("LayoutResX: 1080")
    ass_content.append("LayoutResY: 1920")
    ass_content.append("PlayResX: 1080")
    ass_content.append("PlayResY: 1920")
    ass_content.append("ScaledBorderAndShadow: yes")
    ass_content.append("ScriptType: v4.00+")
    ass_content.append("WrapStyle: 0")
    ass_content.append("YCbCr Matrix: None")
    ass_content.append("")
    ass_content.append("[Kdenlive Extradata]")
    ass_content.append("MaxLayer: 0")
    ass_content.append("DefaultStyles: Default")
    ass_content.append("")
    ass_content.append("[V4+ Styles]")
    ass_content.append("Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding")
    ass_content.append(ASS_STYLE)
    ass_content.append("")
    ass_content.append("[Events]")
    ass_content.append("Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text")
    
    current_offset = 0
    for script_num in sorted(tts_durations.keys()):
        srt_file = os.path.join(workspace, "tts", f"tts_{script_num:03d}.srt")
        subtitles = parse_srt_file(srt_file)
        for sub in subtitles:
            start_time = current_offset + sub["start"]
            end_time = current_offset + sub["end"]
            start_ass = seconds_to_ass_time(start_time)
            end_ass = seconds_to_ass_time(end_time)
            ass_content.append(f"Dialogue: 0,{start_ass},{end_ass},Default,,0,0,0,,{sub['text']}")
        current_offset += tts_durations[script_num]
    
    return "\n".join(ass_content)


def generate_markers_json(script_titles, tts_durations):
    markers = []
    current_pos = 0
    for script_num in sorted(script_titles.keys()):
        if script_num in tts_durations:
            markers.append({
                "pos": seconds_to_frames(current_pos),
                "comment": script_titles[script_num],
                "duration": 0,
                "type": 0
            })
            current_pos += tts_durations[script_num]
    return json.dumps(markers)


# ==============================
# Main Entry Point
# ==============================

def generate_kdenlive_project(workspace):
    """Main function to generate Kdenlive project."""
    print("Lambda Cut Kdenlive Automation")
    print("=" * 50)
    
    # Find base project
    base_project = find_base_project()
    if not base_project:
        print(f"Error: No base project found in {OUTPUT_DIR}")
        return None
    
    print(f"Using base project: {os.path.basename(base_project)}")
    
    # Collect data
    print("\nCollecting data...")
    script_titles = get_script_titles(workspace)
    print(f"  Scripts found: {len(script_titles)}")
    
    tts_durations = get_tts_durations(workspace)
    print(f"  TTS files found: {len(tts_durations)}")
    
    if not script_titles or not tts_durations:
        print("Error: Missing required files (scripts or TTS)")
        return None
    
    total_duration = sum(tts_durations.values())
    print(f"  Total duration: {total_duration:.2f}s ({total_duration/60:.1f}m)")
    
    # Create output directory
    output_dir = os.path.join(workspace, "output")
    os.makedirs(output_dir, exist_ok=True)
    
    # Generate filenames with timestamp
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    project_file = os.path.join(output_dir, f"Shorts Project_{timestamp}.kdenlive")
    ass_file = os.path.join(output_dir, f"Shorts Project_{timestamp}.kdenlive.ass")
    
    # Generate markers
    markers_json = generate_markers_json(script_titles, tts_durations)
    
    # Generate ASS subtitles
    print("Generating ASS subtitles...")
    ass_content = generate_ass_subtitles(workspace, script_titles, tts_durations)
    with open(ass_file, "w") as f:
        f.write(ass_content)
    print(f"  Subtitles saved: {ass_file}")
    
    # Save base project as output (simplified)
    import shutil
    shutil.copy2(base_project, project_file)
    print(f"  Project saved: {project_file}")
    
    # Summary
    print("\n" + "=" * 50)
    print("Summary:")
    print(f"  Scripts: {len(script_titles)}")
    print(f"  TTS files: {len(tts_durations)}")
    print(f"  Total duration: {total_duration:.2f}s ({total_duration/60:.1f}m)")
    print(f"\nOutput files:")
    print(f"  {os.path.basename(project_file)}")
    print(f"  {os.path.basename(ass_file)}")
    
    return project_file


# ==============================
# CLI Entry Point
# ==============================

if __name__ == "__main__":
    # Get workspace from environment or use default
    workspace = os.environ.get("LAMBDA_CUT_WORKSPACE")
    if not workspace:
        script_dir = os.path.dirname(os.path.abspath(__file__))
        workspace = os.path.dirname(script_dir)
    
    generate_kdenlive_project(workspace)
